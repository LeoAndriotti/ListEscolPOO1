    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        txtListaGeral.setText(obj.listar());
        int cod;
        cod = Integer.parseInt(JOptionPane.showInputDialog("Qual indice você quer remover?"));
        if(JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover esse número ","CONFIRME",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
           obj.remover(cod-1);
           txtListaGeral.setText(obj.listar());
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

