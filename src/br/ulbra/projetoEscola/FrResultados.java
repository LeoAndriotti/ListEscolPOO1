    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        btnListar.doClick();
        int cod, novoNum;
        cod = Integer.parseInt(JOptionPane.showInputDialog("Qual índice? "))-1;
        novoNum = Integer.parseInt(JOptionPane.showInputDialog("Qual o novo número? "));
        obj.atulizar(cod, novoNum);
        txtListaGeral.setText(obj.listar());
    }//GEN-LAST:event_btnAtualizarActionPerformed

